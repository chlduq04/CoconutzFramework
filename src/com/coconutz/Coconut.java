package com.coconutz;


public class Coconut<T> {
	
	private T value;
	private String type;

	public Coconut(String str, T obj){
		type = str;
		value = obj;
	}
	public String getString(){
		return value.toString();
	}
	public String type(){
		return type;
	}
	public T value(){
		return value;
	}
}
